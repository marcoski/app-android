// VERSIONE WHILE DO
console.log('VERSIONE WHILE')
let x = 1
while (x <= 20) {
  console.log(x)
  x += 1
}

// VERSIONE FOR
console.log('VERSIONE FOR')
for (let x = 0; x <= 20; x++) {
  console.log(x)
}
